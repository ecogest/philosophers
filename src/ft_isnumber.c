int	ft_isnumber(const char *str)
{
	int	i;

	i = 0;
	if (str[0] == '-' || str[0] == '+')
		i++;
	while (1)
	{
		if (str[i] < '0' || str[i] > '9')
			return (0);
		i++;
		if (!str[i])
			break ;
	}
	return (1);
}
